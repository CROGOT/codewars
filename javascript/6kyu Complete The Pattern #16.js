function pattern(n){
  var arr=Array.from({length: n},c=>{
    c=[];
    for (let i=1; i<=n; i++) c[n-i]= i%10;
    return c;
  });
 
  for (let i=0; i<n; i++)
    for (let j=n-i; j<n; j++) arr[n-i-1][j]=arr[n-i-1][n-i-1];
  return arr.map(c=>c.reduce((a,v)=>a+v,'')).join('\n');
}

